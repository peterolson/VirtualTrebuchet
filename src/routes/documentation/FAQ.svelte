<h1>Frequently Asked Questions</h1>

<p>
	<strong
		>I am building a trebuchet for a school project. Can you help me design my trebuchet?</strong
	>
</p>

<p>
	If you have a specific question, I will usually answer it, but I will not do any general design or
	optimization work for you.
</p>

<p><strong>Can you give me the equations for a trebuchet?</strong></p>

<p>
	I have the equations of motion for a trebuchet in the
	<a href="/documentation/explanation/EquationsOfMotion">Equations of Motion</a> section. I would
	suggest working through this
	<a href="/documentation/explanation/ExampleProblem">Example Problem</a> to understand the basics
	of how the equations work first. I have done my best to make the explanation of the equations easy
	to follow, but they do involve some linear algebra and some fairly lengthy differential equations.
</p>

<p><strong>How close are VirtualTrebuchet&#39;s predictions to reality?</strong></p>

<p>
	In general, VirtualTrebuchet will over predict the performance of a trebuchet by some amount
	because it ignores things like friction. You can look at the
	<a href="/user-projects">User Projects</a> page to see how VirtualTrebuchet&#39;s predictions have
	compared to some real trebuchets that VirtualTrebuchet users have made. To get a good prediction
	from VirtualTrebuchet, you need make sure you understand all the input, and use accurate values.
	If you put garbage in you will get garbage out.
</p>
