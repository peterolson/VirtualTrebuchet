<h1>Negative Mass/Inertia</h1>

<p>
	You have entered a negative mass or inertia. This simulation cannot handle negative masses of
	inertias because it assumes
	<a href="http://en.wikipedia.org/wiki/Newtons_laws_of_motion">Newton’s laws of motion</a>.
</p>

<p>
	To see the problem, we will look at Newton’s second law which states that force is equal to mass
	times acceleration, i.e.,
</p>

<p><img alt="Newton&#39;s Second Law" src="/documentation/Fma.png" /></p>

<p>
	Under normal circumstances (if the mass is positive), this relation tells us that if a body is
	acted upon by a force, than the body will accelerate in the direction in which the force is
	applied. For example, if a ball is released above the ground, then gravity will pull down on it
	causing it to accelerate toward the ground.
</p>

<p><img alt="Normal Acceleration" src="/documentation/NegativeMass1.png" /></p>

<p>
	However, given the same situation excepting that the mass of the ball is negative, the ball will
	accelerate upwards.
</p>

<p><img alt="Normal Acceleration" src="/documentation/NegativeMass2.png" /></p>

<p>
	In this case, the result seems somewhat reasonable. However if we look at a different example, the
	problem becomes more apparent. In this example, we will look a bat hitting a ball. If the mass of
	the ball is negative in this case, the force of the bat acting on the ball will cause the ball to
	accelerate towards the bat. The problem is that the ball is already touching the bat, which means
	that to accelerate towards the bat; the ball would have to go through the bat, which cannot happen
	without completely destroying the ball and/or the bat.
</p>

<p><img alt="Acceleration from a negative mass" src="/documentation/NegativeMass3.png" /></p>

<p>Because of this problem, this simulation cannot handle negative masses meaningfully.</p>

<p>
	Inertia is the rotational version of mass, so the same problems applies to negative inertias also.
</p>
