<h1>Projectile Type</h1>

<p>
	Selecting a <strong>Projectile</strong> from the drop down list of projectiles will automatically
	fill out the <a href="/documentation/inputs/projectile/MassProjectile">Mass of Projectile</a> and
	<a href="/documentation/inputs/projectile/ProjectileDiameter">Projectile Diameter</a> inputs. If
	the projectile you are using is not on the list, choose the custom option.
</p>

<p><img alt="Projectile" src="/documentation/MassProjectile.png" /></p>

<p>
	The baseball and golf ball options are special cases. They each have their own curve for
	calculating the <a href="http://en.wikipedia.org/wiki/Drag_coefficient">drag coefficient</a>. For
	all the other options, the simulation uses the smooth sphere curve for calculating the drag
	coefficient. For more information about calculating the drag coefficient see the
	<a href="/documentation/explanation/ProjectileDrag">Projectile Drag</a> section.
</p>
