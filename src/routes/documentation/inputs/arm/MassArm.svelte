<h1>Mass of Arm</h1>

<p>
	<strong>Mass of Arm</strong> is the mass of the entire arm, which includes the parts defined by
	<a href="/documentation/inputs/trebuchet-dimensions/ArmLengths">Length of Short Arm</a> and
	<a href="/documentation/inputs/trebuchet-dimensions/ArmLengths">Length of Long Arm</a>.
</p>

<p><img alt="Trebuchet Arm" src="/documentation/MassArm.png" /></p>

<p>For the best performance, the arm should be as light as possible.</p>
