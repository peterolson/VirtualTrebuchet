<h1>Arm Lengths</h1>

<p>
	The arm of the trebuchet is comprised of two sections: the long component of the arm which is the
	section that goes from the pivot point to the end where the sling attaches to the arm, and the
	short component which is the section that goes from the pivot to the end where the weight
	attaches.
</p>

<p>The length of the long component of the arm is called <strong>Length of Long Arm</strong>.</p>

<p>The length of the short component of the arm is called <strong>Length of Short Arm</strong>.</p>

<p><img alt="Trebuchet Arm Lengths" src="/documentation/ArmLengths.png" /></p>

<p>
	The optimal ratio between the long part of the arm and the short part of the arm will depend
	mainly on the <a href="/documentation/inputs/weight/MassWeight">Mass of Weight</a>. If the weight
	is very heavy, the long part of the arm can be many times the length of the short part of the arm,
	which would allow the projectile to attain high velocities.
</p>
