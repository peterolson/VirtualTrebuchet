<h1>Height Of Pivot</h1>

<p><strong>Height of Pivot</strong> is the distance from the ground to the pivot.</p>

<p>
	The pivot is where the arm connects to the frame of the trebuchet, and the point about which the
	arm rotates.
</p>

<p><img alt="Height of Pivot" src="/documentation/HeightOfPivot.png" /></p>

<p>
	<strong>Note:</strong> To prevent the weight from hitting the ground, the height of the pivot
	should be greater than the sum of the
	<a href="/documentation/inputs/trebuchet-dimensions/ArmLengths">Length of Short Arm</a> and the
	total length of the weight.
</p>
