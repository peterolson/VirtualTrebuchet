<h1>Release Angle</h1>

<p>
	The <strong>Release Angle</strong> is the angle that the velocity vector of the projectile reaches
	before the projectile is released.
</p>

<p><img alt="Release Angle" src="/documentation/ReleaseAngle.png" /></p>

<p>
	<strong>Note:</strong> The angle of the velocity of the projectile is not quite the same as the
	angle of a vector perpendicular to the sling. The angle perpendicular to the sling is the velocity
	of the projectile relative to the end of the arm, but the end of the arm is also moving, so the
	velocity of the projectile will be the vector sum of the velocity at the end of the arm and the
	relative velocity of the projectile.
</p>

<p><img alt="Relative Velocities" src="/documentation/ReleaseAngleExplination.png" /></p>

<p>The projectile typically goes the farthest if the release angle is between 35deg and 40deg.</p>
